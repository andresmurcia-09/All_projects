# Operadores Artiméticos

a = 10
b = 3

# Suma +
suma = a + b
print(f"Suma: {suma}")

# Resta -
resta = a - b
print(f"Resta: {resta}")

# Multiplicación *
multiplicacion = a * b
print(f"Multiplicacion: {multiplicacion}")

# División / (retorna un tipo float)
division = a / b
print(f"Division: {division:.2f}")

# División entera (//)
division_entera = a // b
print(f"Division entera: {division_entera}")

# Modulo (%) residuo de la división
modulo = a % b
print(f"Residuo de la division: {modulo}")

# Exponente (potencia) **
exponente = a**b  # 10 elevado a la 3 = 10*10*10=1000
print(f"Exponente: {exponente}")

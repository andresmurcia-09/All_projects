numero_de_la_semana = int(input("Ingrese un número de la semana entre 1 y 7: "))

if numero_de_la_semana == 1:
    print("Es lunes")
elif numero_de_la_semana == 2:
    print("Es martes")
elif numero_de_la_semana == 3:
    print("Es miercoles")
elif numero_de_la_semana == 4:
    print("Es jueves")
elif numero_de_la_semana == 5:
    print("Es viernes")
elif numero_de_la_semana == 6:
    print("Es sabado")
elif numero_de_la_semana == 7:
    print("Es domingo")
else:
    print("No digitó un número correcto")
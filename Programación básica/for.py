# Ejemplo 1
# Itera desde 0 hasta 4 (5-1)
for i in range(5):
    # Imprime el valor de la variable i en cada iteración
    print(i)

# Ejemplo 2
nombres = ["Ana", "Juan", "Luis", "Sofía"]
# Itera sobre la lista de nombres
for nombre in nombres:
    # Imprime el nombre en cada iteración
    print(nombre)

# Ejemplo 3
# Itera desde 1 hasta 10 (11-1) en pasos de 2
for i in range(1, 11, 2):
    # Imprime el valor de la variable i en cada iteración
    print(i)
